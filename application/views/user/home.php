		document.addEventListener('DOMContentLoaded', () => {
    const sickNewsItems = document.querySelectorAll('.sick-news');
    const linkNewsLists = document.querySelectorAll('.link-news-list');

    // Initially hide all .link-news-list except the first one
    linkNewsLists.forEach((link, index) => {
        link.classList.toggle('show', index === 0); // Only show the first one
    });

    // Add click event listeners to each .sick-news item
    sickNewsItems.forEach((item, index) => {
        item.addEventListener('click', () => {
            // Hide all .link-news-list elements
            linkNewsLists.forEach(link => link.classList.remove('show'));

            // Show the corresponding .link-news-list
            if (linkNewsLists[index]) {
                linkNewsLists[index].classList.add('show');
            }

            // Optional: Highlight the active .sick-news item
            sickNewsItems.forEach(news => news.classList.remove('active'));
            item.classList.add('active');
        });
    });
});


